// Сохранение графика платежей при правке заказа БЕЗ потери истории оплат.
//
// Раньше форма заказа удаляла ВСЕ платежи заказа и пересоздавала их из формы,
// проставляя оплаченным paidAt = «сейчас» и плательщиком — того, кто редактировал.
// Любое сохранение заказа (даже правка заметки) затирало реальные даты оплат фабрике.
//
// Теперь правило: уже оплаченные (PAID) платежи неприкосновенны. Вызывающий код
// удаляет только PENDING-строки, а здесь мы решаем, какие входящие строки формы
// нужно пересоздать: те, что ссылаются (по id) на уже оплаченный платёж, мы НЕ
// трогаем — он сохраняется в базе как есть (со своей датой и плательщиком).

export type IncomingPayment = {
  /** id существующего платежа (если строка пришла из БД), либо синтетический/пустой для новых */
  id?: string | null;
  plannedDate: string;
  amount: number;
  label: string;
  paid?: boolean;
};

/**
 * Из входящих строк формы оставляет те, что нужно создать заново.
 * Строки, чей id совпадает с уже оплаченным платежом, исключаются — такой платёж
 * сохраняется вызывающим кодом нетронутым (его не удаляют и не пересоздают).
 *
 * @param incoming        платежи из формы (могут нести id существующих строк)
 * @param existingPaidIds id платежей заказа, у которых статус PAID (их бережём)
 */
export function selectOrderPaymentsToCreate(
  incoming: IncomingPayment[],
  existingPaidIds: ReadonlySet<string>,
): IncomingPayment[] {
  return incoming.filter((p) => !(p.id && existingPaidIds.has(p.id)));
}
