// Нормализация: убираем лишние пробелы, «ё»→«е», приводим к нижнему регистру
function normalize(s: string): string {
  return s.trim().toLowerCase().replace(/ё/g, "ё"); // ё оставляем, у нас оба варианта в словаре
}

export function colorHexFromName(colorName: string | null | undefined): string {
  if (!colorName) return "#94a3b8"; // slate-400
  const key = normalize(colorName);

  // Точное совпадение
  if (COLOR_MAP[key]) return COLOR_MAP[key];

  // Попробуем найти по ключевым словам (любое включение)
  for (const [name, hex] of Object.entries(COLOR_MAP)) {
    if (key.includes(name) || name.includes(key)) return hex;
  }

  return "#94a3b8"; // fallback — нейтральный серый
}

// Определяем, нужна ли тёмная или светлая подложка для контраста
export function isLightColor(hex: string): boolean {
  const h = hex.replace("#", "");
  const r = parseInt(h.substring(0, 2), 16);
  const g = parseInt(h.substring(2, 4), 16);
  const b = parseInt(h.substring(4, 6), 16);
  // Простейшая формула luminance
  const lum = (0.299 * r + 0.587 * g + 0.114 * b) / 255;
  return lum > 0.6;
}
